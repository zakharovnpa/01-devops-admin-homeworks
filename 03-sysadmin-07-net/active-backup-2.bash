Обычно это предпочтительный режим, особенно если коммутаторы имеют ISL и хорошо работают вместе. 
Если конфигурация сети такова, что один коммутатор является специально резервным коммутатором 
(например, имеет меньшую емкость, более высокую стоимость и т. Д.), То можно использовать основной 
вариант, чтобы гарантировать, что предпочтительный канал всегда используется, когда он доступен.
