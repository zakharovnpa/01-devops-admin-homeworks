Этот режим действительно является режимом специального назначения и подходит 
только для очень специфических нужд. Например, если два коммутатора не подключены (нет ISL), 
а сети за ними полностью независимы. В этом случае, если необходимо, чтобы какой-то конкретный 
односторонний трафик достигал обеих независимых сетей, то режим широковещания может быть подходящим.
